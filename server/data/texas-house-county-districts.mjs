/** Reverse lookup: house district number → county names (sorted). */
export function houseDistrictCountiesMap() {
  const map = new Map();
  for (const { county, districts } of COUNTY_HOUSE_DISTRICTS) {
    for (const district of districts) {
      if (!map.has(district)) map.set(district, []);
      map.get(district).push(county);
    }
  }
  for (const [district, counties] of map) {
    map.set(
      district,
      [...new Set(counties)].sort((a, b) => a.localeCompare(b))
    );
  }
  return map;
}
